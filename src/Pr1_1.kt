fun main() {
    var a: Byte = 125
    var long: Long = 120510
    var short: Short = -2
    var b: Int = 1229
    var c: Float = 21.25f
    var d: Double = 122.96
    var ch: Char = 'A'
    var st: String = "Hello!!!"
    var boolean: Boolean = false
    println("Byte value = $a")
    println("Short value = $short")
    println("Integer value = $b")
    println("Long value = $long")
    println("Float value = $c")
    println("Double value = $d")
    println("Character value = $ch")
    println("String value = $st")
    println("Boolean value = $boolean")
}