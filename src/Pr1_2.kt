fun main() {
    var a: Int = 10
    var b: Double = a.toDouble()
    var str: String = "200"
    var i: Int = str.toInt()
    var i2: Int = str.toInt()
    var d: Double = str.toDouble()
    println("Integer value = $a")
    println("String value = $str")
    println("Int to Double = $b")
    println("String to Int = $i")
    println("String to Int = $i2")
    println("String to Double = $d")
}